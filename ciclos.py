## Estructura para realizar ciclos while en python

x = 0
y = 10

while (x < 5 and y > 5):
    print("hola ", x)
    print("adios ", y)
    y -= 1
    
print("fin del ciclo")
#siempre dar condiciones con fin al ciclo, si no se queda en un bucle infinito
## Esta es la estructura para realizar ciclos en python 

